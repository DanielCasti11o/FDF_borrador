#include <stddef.h>

int	*ft_intchr(int *s, int c)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
	{
		if ((int)c == s[i])
		{
			return ((int *)(s + i));
		}
		i++;
	}
	if ((int)c == '\0')
	{
		return ((int *)(s + i));
	}
	return (NULL);
}
